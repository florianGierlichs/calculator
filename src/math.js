// calculations
export const addition = (numberOne, numberTwo) => {
  return numberOne + numberTwo;
};

export const subtract = (numberOne, numberTwo) => {
  return numberOne - numberTwo;
};

export const divide = (numberOne, numberTwo) => {
  return numberOne / numberTwo;
};

export const multiply = (numberOne, numberTwo) => {
  return numberOne * numberTwo;
};
